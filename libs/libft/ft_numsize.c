#include "libft.h"

size_t	ft_numsize(intmax_t n, int base)
{
	size_t	len;

	if (base < 2 || base > 36)
		return (0);
	len = n < 0 ? 2 : 1;
	while (n /= base)
		len++;
	return (len);
}
