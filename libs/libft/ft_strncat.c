#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = -1;
	while (s1[++i] != 0)
		;
	j = -1;
	if (ft_strlen(s2) != 0)
	{
		while (s2[++j] != 0 && j < n)
			s1[i++] = s2[j];
	}
	s1[i] = 0;
	return (s1);
}
