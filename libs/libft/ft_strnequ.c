#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = -1;
	if (s1 == NULL || s2 == NULL)
		return (-1);
	while ((s1 || s2) && ++i < n)
	{
		if (s1[i] != s2[i])
			return (0);
	}
	return (1);
}
